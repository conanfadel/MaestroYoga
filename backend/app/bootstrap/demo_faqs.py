def seed_default_faqs(center_id: int) -> list[models.FAQItem]:
    return [
        models.FAQItem(
            center_id=center_id,
            question=question,
            answer=answer,
            sort_order=index,
            is_active=True,
        )
        for index, (question, answer) in enumerate(DEFAULT_FAQS, start=1)
    ]
